import { NavLink } from 'react-router-dom'
import { Capacitor } from '@capacitor/core'

const isNative = Capacitor.isNativePlatform()

const tabs = [
  { to: '/',          label: '홈',    icon: HomeIcon },
  { to: '/list',      label: '기록',  icon: ListIcon },
  { to: '/calendar',  label: '캘린더', icon: CalendarIcon },
  { to: '/stats',     label: '집계',  icon: StatsIcon },
  { to: '/settings',  label: '설정',  icon: SettingsIcon },
] as const

export function BottomNav() {
  return (
    <nav
      className="fixed left-0 right-0 z-40 flex justify-center border-t border-warm-200 bg-warm-50"
      style={isNative ? { bottom: 'var(--admob-h, 60px)' } : { bottom: 0 }}
    >
      <ul className="flex w-full max-w-md">
        {tabs.map(({ to, label, icon: Icon }) => (
          <li key={to} className="flex-1">
            <NavLink
              to={to}
              end={to === '/'}
              className={({ isActive }) =>
                [
                  'flex flex-col items-center gap-0.5 py-2.5 text-xs transition-colors',
                  isActive ? 'text-primary-500' : 'text-[#8a7570]',
                ].join(' ')
              }
            >
              <Icon />
              <span>{label}</span>
            </NavLink>
          </li>
        ))}
      </ul>
    </nav>
  )
}

